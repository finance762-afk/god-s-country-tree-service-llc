        <p class="lead">A dangerous tree shows visible structural defects, decay, or instability that increase the risk of failure. Large dead branches, trunk cracks, fungal growth at the base, sudden lean, cavities, root exposure, and splitting co-dominant stems are the most common warning signs. If your tree exhibits multiple symptoms or poses a direct threat to your home, power lines, or occupied areas, schedule a professional tree risk assessment immediately.</p>

