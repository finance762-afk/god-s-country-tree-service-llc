        <h2>1. Large Dead or Hanging Branches</h2>
        <p>Dead branches don't bend — they snap. A 4-inch diameter dead limb 30 feet up weighs 50-100+ pounds and can punch through a roof or windshield when it falls. In DeLand's storm country, dead branches become projectiles in 60+ mph winds.</p>

        <p>Check for:</p>
        <ul>
          <li><strong>No leaves or buds</strong> — while surrounding branches are green and leafed out</li>
          <li><strong>Brittle bark</strong> — bark that flakes off easily or shows exposed dry wood underneath</li>
          <li><strong>Hanging or broken limbs</strong> — partially detached branches caught in the canopy</li>
        </ul>

        <p>One or two small dead twigs are normal. Multiple large dead branches scattered through the canopy signal decline or disease. If dead branches are concentrated on one side of the tree, suspect root damage or trunk decay on that side. <a href="/blog/best-time-trim-trees-florida/">Schedule trimming</a> to remove deadwood before hurricane season — dead limbs are the first to fail in storms.</p>

        <h2>2. Cracks or Seams in the Trunk</h2>
        <p>Vertical cracks, horizontal seams, or separating bark indicate internal structural failure. A crack running up the trunk can widen suddenly under wind load, splitting the tree in half. Frost cracks (vertical splits caused by winter temperature swings) are common in thin-barked trees like maples but less common in Central Florida — if you see a crack in a DeLand oak or pine, suspect decay or storm damage, not frost.</p>

        <p>Serious trunk cracks:</p>
        <ul>
          <li>Extend more than 12 inches vertically or horizontally</li>
          <li>Penetrate deeper than the bark layer (you can see wood fiber inside)</li>
          <li>Occur near the base of the tree where leverage is greatest</li>
          <li>Have fungal growth or dark staining around the edges (indicates internal decay)</li>
        </ul>

        <p>A crack wider than 1 inch at any point requires immediate evaluation. Don't wait until the next storm to find out whether the tree will hold together.</p>

        <h2>3. Mushrooms or Fungal Growth Around the Trunk or Roots</h2>
        <p>Mushrooms sprouting from the trunk, root flare, or soil near the base signal internal decay. The visible mushroom is the fruiting body — the actual fungus has been digesting the tree's wood for months or years. By the time mushrooms appear, significant structural loss has already occurred.</p>

        <p>Common decay fungi in DeLand trees:</p>
        <ul>
          <li><strong>Artist's conk</strong> — large, hard, woody shelf fungi on trunks (indicates advanced heartwood decay)</li>
          <li><strong>Honey fungus</strong> — yellowish mushrooms in clusters at the base (aggressive root and trunk decay)</li>
          <li><strong>Chicken of the woods</strong> — bright orange/yellow shelf fungi (causes brown cubical rot)</li>
        </ul>

        <p>Not all fungi kill trees immediately, but their presence means the tree's structural integrity is compromised. A tree with extensive fungal decay may look healthy in the canopy but lack the internal strength to withstand wind loads. If you see mushrooms, schedule a <a href="/services/tree-health-assessments/">tree risk assessment</a> — an arborist can use a resistograph or sonic tomograph to measure the extent of internal decay.</p>

        <h2>4. Cavities and Hollow Areas</h2>
        <p>A cavity is a hollow area inside the trunk or a major branch, usually caused by decay following an injury or pruning wound. Small cavities (less than 1/3 of the trunk's diameter) may not significantly weaken the tree. Large cavities (more than half the trunk's diameter) drastically reduce strength.</p>

        <p>Warning signs:</p>
        <ul>
          <li>Visible holes or openings in the trunk</li>
          <li>Soft, spongy wood when you press on the bark</li>
          <li>Sawdust or wood shavings at the base (carpenter ants or beetles excavating decay)</li>
          <li>Animals nesting inside the cavity (woodpeckers, squirrels, raccoons use hollow trees)</li>
        </ul>

        <p>A hollow tree can stand for years if the remaining shell is thick enough and the tree isn't in a high-wind exposure area. But in DeLand's hurricane belt, a hollow oak or pine is a gamble. <a href="/blog/tree-removal-cost-deland-fl/">The cost of removing a hollow tree</a> is far less than the cost of repairing the roof it falls through.</p>

        <h2>5. Raised Soil, Exposed Roots, or Root Damage</h2>
        <p>Roots anchor the tree and absorb water and nutrients. When you see roots heaving out of the ground, soil mounding on one side of the trunk, or exposed roots with broken ends, the tree's stability is compromised.</p>

        <p>Root damage causes:</p>
        <ul>
          <li><strong>Construction or trenching</strong> — digging within the tree's drip line severs roots and destabilizes the tree</li>
          <li><strong>Soil erosion</strong> — heavy rain washing away soil exposes and damages roots</li>
          <li><strong>Soil compaction</strong> — vehicle traffic or heavy equipment compacting soil suffocates roots</li>
          <li><strong>Root rot</strong> — waterlogged soil or fungal pathogens killing roots</li>
        </ul>

        <p>If the soil is mounding or roots are lifting on the side opposite the lean, the tree is beginning to uproot. This is an emergency — the tree can fail suddenly, especially in saturated soil after heavy rain. Do not wait for the next storm to address a tree showing root heave.</p>

        <h2>6. New or Worsening Lean</h2>
        <p>Many trees naturally lean slightly — that's not necessarily dangerous. A sudden lean or a lean that's worsening over time signals root failure or soil instability. If a tree that stood straight last year is now leaning 10-15 degrees toward your house, it's failing.</p>

        <p>Distinguish normal lean from hazard lean:</p>
        <ul>
          <li><strong>Normal lean:</strong> Tree has leaned the same direction for years, trunk taper is symmetrical, no soil cracking or root exposure at the base</li>
          <li><strong>Hazard lean:</strong> Lean appeared suddenly or worsened after a storm, soil is cracked or mounded on the compression side (opposite the lean), roots are exposed or lifting on the tension side</li>
        </ul>

        <p>A tree leaning more than 15 degrees toward a target (house, driveway, neighbor's property, power lines) should be evaluated immediately. Lean toward an open field or woods is less critical, but lean toward any occupied or high-value area is a removal candidate.</p>

        <h2>7. Multiple Trunks Splitting Apart (Co-Dominant Stems)</h2>
        <p>Trees with two or more trunks of similar size growing from the same point often develop a weak union where the trunks join. As the trunks grow larger, bark becomes included in the union, preventing strong wood-to-wood connection. Under wind or ice load, co-dominant stems can split apart, dropping half the tree.</p>

        <p>High-risk co-dominant stems:</p>
        <ul>
          <li>Trunks form a narrow V-angle (less than 45 degrees) instead of a wide U-shape</li>
          <li>Bark is visible in the crotch between trunks (included bark = weak union)</li>
          <li>Cracks are developing where the trunks join</li>
          <li>One trunk is significantly heavier or taller than the other (unbalanced load)</li>
        </ul>

        <p>Some co-dominant stems can be cabled or braced to reduce failure risk, but if the union is already cracking or one trunk is leaning away from the other, removal is the safer option. Don't gamble on a <a href="/blog/best-time-trim-trees-florida/">poorly structured tree</a> above your home.</p>

        <h2>8. Sudden Leaf Loss, Canopy Dieback, or Off-Season Color Change</h2>
        <p>A tree that suddenly drops leaves in summer, develops large areas of dead canopy, or turns brown/yellow out of season is stressed or diseased. Possible causes include:</p>
        <ul>
          <li><strong>Drought stress</strong> — extended dry periods kill fine roots and trigger leaf drop</li>
          <li><strong>Root damage</strong> — construction, trenching, or soil compaction</li>
          <li><strong>Vascular diseases</strong> — oak wilt, laurel wilt, verticillium wilt block water and nutrient transport</li>
          <li><strong>Insect infestation</strong> — bark beetles, borers, or defoliators</li>
        </ul>

        <p>If more than 25% of the canopy is dead or dying, the tree is in severe decline. A tree losing 50%+ of its canopy in one season is unlikely to recover and poses an increasing hazard as dead branches accumulate. Monitor for a few weeks — if the tree doesn't refoliate or continues to decline, schedule an arborist inspection to determine whether treatment is possible or removal is necessary.</p>

        <h2>9. Damage After Heavy Rain, Wind, or Lightning</h2>
        <p>Inspect trees after every major storm. Wind, heavy rain, and lightning cause hidden damage that may not be obvious from the ground. Post-storm warning signs:</p>
        <ul>
          <li><strong>Broken or hanging branches</strong> — even if they haven't fallen yet</li>
          <li><strong>Stripped bark or torn limbs</strong> — where branches were ripped off by wind</li>
          <li><strong>New lean</strong> — the tree shifted in saturated soil</li>
          <li><strong>Lightning strike scars</strong> — vertical strips of missing bark, scorched wood, or exploded trunk sections</li>
          <li><strong>Soil cracks or root exposure</strong> — the tree began to uproot but didn't fall completely</li>
        </ul>

        <p>A tree that survived a storm but shows significant damage is more likely to fail in the next storm. <a href="/blog/insurance-fallen-tree-removal-florida/">If storm damage is severe enough to threaten structures</a>, document it with photos and contact your homeowners insurance — some policies cover preventive removal of trees damaged by covered perils.</p>

        <h2>What to Do If Your Tree Is Dangerous</h2>

        <h3>Step 1: Document the Hazard</h3>
        <p>Take photos of cracks, decay, lean, dead branches, and any visible defects. Photograph the tree from multiple angles and include reference objects (house, fence, driveway) to show proximity to targets. This documentation supports insurance claims if the tree later causes damage.</p>

        <h3>Step 2: Keep People and Vehicles Away</h3>
        <p>If the tree poses an immediate threat, don't park under it, don't let children play near it, and avoid spending time in the target zone. If limbs are actively cracking or the tree is visibly unstable, cordon off the area and call an emergency tree service.</p>

        <h3>Step 3: Schedule a Professional Tree Risk Assessment</h3>
        <p>An ISA-certified arborist can perform a Level 2 or Level 3 tree risk assessment using visual inspection, resistance drilling, sonic tomography, or pull testing to measure structural integrity. The arborist will evaluate:</p>
        <ul>
          <li>Likelihood of failure (how soon the tree is likely to fail)</li>
          <li>Size of part likely to fail (whole tree, trunk, major limb, branch)</li>
          <li>Target (what the tree or part would hit if it failed)</li>
          <li>Consequences (property damage, injury, death)</li>
        </ul>

        <p>Based on the assessment, the arborist will recommend monitoring, pruning, cabling, or removal. Get the recommendation in writing — if you later need to file an insurance claim or apply for a <a href="/blog/tree-removal-permit-deland-fl/">tree removal permit</a>, the arborist's report supports your case.</p>

        <h3>Step 4: Act Before the Next Storm</h3>
        <p>Hurricane season runs June 1 to November 30 in Florida. A tree that's marginally stable in calm weather can fail catastrophically in 75+ mph winds. Don't wait until a hurricane is 48 hours out to call a tree service — by then, crews are booked solid and <a href="/blog/tree-removal-cost-deland-fl/">emergency removal rates</a> are 2-3× normal pricing.</p>

        <p>If an arborist identifies a tree as high risk, schedule removal or mitigation work during the off-season (December through May) when crews have availability and pricing is more competitive.</p>

